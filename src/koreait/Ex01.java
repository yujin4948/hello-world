public class Ex01 { // Ex01 클래스 시작

	public static void main(String[] arg) { // main() 메서드 시작
		
		System.out.println("자바 프로그램에 오신 여러분을 환영합니다.");
		System.out.print("반갑습니다.");
		System.out.print("지금 비가 오네요.");
		System.out.println();
		System.out.println("오늘은 날씨가 덥네요...");
		System.out.println("수정수정!");
		
		/*
		 * System.out : 표준출력장치(모니터, 콘솔)
		 * println() : 지정된 장치에 결과를 출력하는 메서드
		 *             => ln: line(출력 후 줄바꿈)
		 * print() : 다음 줄로 이동하지 않고 옆(오른쪽)에 출력
		 */
		
		/*
		 * 이클립스 출력 관련 단축키
		 * syso + Ctrl + space ==> 출력문 단축키
		 */
	} // main() 메서드 끝
	
} // Ex01 클래스 끝
